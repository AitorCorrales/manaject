package essentials;

public class Recommended {
	private String key;
	private double punct;
	public Recommended(String key, double punct){
		this.key = key;
		this.punct = punct;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public double getPunct() {
		return punct;
	}
	public void setPunct(double punct) {
		this.punct = punct;
	}

}
